typedef struct{
    char number;
    char letter;
    char color;
    char x,y;
    char rotation;
    char *rotations;
} TPiece;
TPiece* create_piece(char piece_letter){
    
    TPiece pieceL={
        1,          //number
        'L',        //letter
        1,          //color
        0,0,        //x,y
        0,        //rotation
        &lRotations[0][0][0]
    };
    TPiece pieceJ={
        2,          //number
        'J',        //letter
        2,          //color
        0,0,        //x,y
        0,         //rotation
        &jRotations[0][0][0]
    };
    TPiece pieceZ={
        3,          //number
        'Z',        //letter
        3,          //color
        0,0,        //x,y
        0,          //rotation
        &zRotations[0][0][0]
    };
    TPiece pieceS={
        4,          //number
        'S',        //letter
        4,          //color
        0,0,        //x,y
        0,          //rotation
        &sRotations[0][0][0]
    };
    TPiece pieceT={
        5,          //number
        'T',        //letter
        5,          //color
        0,0,        //x,y
        0,          //rotation
        &tRotations[0][0][0]
    };
    TPiece pieceO={
        6,          //number
        'O',        //letter
        6,          //color
        0,0,        //x,y
        0,          //rotation
        &oRotations[0][0][0]
    };
    TPiece pieceI={
        7,          //number
        'I',        //letter
        7,          //color
        0,0,        //x,y
        0,        //rotation
        &iRotations[0][0][0]
    };
    TPiece *piece=NULL;
    if(piece_letter=='L')
        piece=&pieceL;
    else if(piece_letter=='J')    
        piece=&pieceJ;
    else if(piece_letter=='Z')    
        piece=&pieceZ;
    else if(piece_letter=='S')    
        piece=&pieceS;
    else if(piece_letter=='T')    
        piece=&pieceT;
    else if(piece_letter=='O')    
        piece=&pieceO;
    else if(piece_letter=='I')    
        piece=&pieceI;
    return piece;   
}